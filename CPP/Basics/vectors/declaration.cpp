#include<iostream>
#include<vector> 

using namespace std;

int main()
{
    vector<int> a;
    // push_back() is used for appending elements onto the vector
    a.push_back(10);
    // pop_back() is used for removing last element from the vector
    a.pop_back(10);
    // size() is used to retrive the no of elements in the vector
    a.size();
    
}