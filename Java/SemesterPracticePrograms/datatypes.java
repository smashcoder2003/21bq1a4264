import java.util.*;

public class datatypes {
    public static void main (String[] args) {
        byte very_small_int = 127; // This is a very small integer
        short small_int = 1023; // This is a small integer
        int a = 1000000; // This is an integer
        long ll = 123091241; // This is a large integer
        float b = (float) 1.5; // This is a decimal value
        double c = 1.1414124141; // This is a more precise decimal value
        String s = "This is a string";
        char ch = 'A'; // This is a single character
    }
}

