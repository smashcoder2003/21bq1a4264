#include <iostream>
using namespace std;

int main()
{
    int a;
    a = 10;
    cout << a << " " <<sizeof(a)<<endl;

    float b;
    b = 10.123;
    cout << b << " " << sizeof(b)<<endl;

    long int c;
    c = 1241514;
    cout << c << " "<< sizeof(c)<<endl;

    short int d;
    d = 1;
    cout<<d<<" "<<sizeof(d)<<endl;
}